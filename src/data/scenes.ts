export function chooseEnding(stats: GameStats): Ending {
  const crueltyScore = stats.authority + stats.suspicion - stats.humanity - stats.truth;

  if (stats.authority >= 9 && stats.suspicion >= 8 && stats.humanity <= 2) {
    return endings.broken;
  }

  if (stats.truth >= 9 && stats.humanity >= 6) {
    return endings.unwritten;
  }

  if (stats.humanity >= 7 && stats.shame >= 6) {
    return endings.laughter;
  }

  if (crueltyScore >= 9 || (stats.authority >= 9 && stats.truth <= 2)) {
    return endings.position;
  }

  if (stats.shame >= 7 && stats.humanity <= 5) {
    return endings.empty;
  }

  if (stats.truth >= 7 && stats.humanity >= 5) {
    return endings.unwritten;
  }

  if (stats.humanity + stats.shame >= stats.authority + stats.suspicion) {
    return endings.laughter;
  }

  return endings.empty;
}
